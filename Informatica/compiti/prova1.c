#include <stdio.h>

void modifica(int mat[][3], int r)
{
   for(int i = 0; i < r; i++)
   {
    mat[i][i] += 10;
   }
}

int main()
{
    int mat[3][3]= {{1,2,3}, {4, 5, 6}, {7, 8, 9}};
    modifica(mat, 3);
    
    for(int i=0; i < 3; i++)
    {
        for(int j=0; j<3; j++)
        {
            printf("%d ", mat[i][j]);
        }
        printf("\n");
    }

    return 0;
}